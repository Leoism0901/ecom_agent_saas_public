# 1. 订单状态查询
# ============================================================

def get_order_status(order_id: str) -> dict:
    """
    【订单物流状态查询】查询指定订单的当前物流配送进度与快递承运信息。

    触发意图（大模型 Tool-Calling 核心决策依据 —— 必须严格遵守）：
        大模型在接收到买家消息时，若消息语义匹配以下任一场景，**必须**调用本工具：
        ┌─────────────────────────────────────────────────────────────────┐
        │ 场景 A · 主动查物流：                                          │
        │   "我的快递到哪了"、"查一下物流"、"包裹到哪里了"                 │
        │   "帮我看看订单到哪了"、"物流信息"、"快递进度"                   │
        │                                                                 │
        │ 场景 B · 发货时效询问：                                        │
        │   "发货了吗"、"什么时候发货"、"怎么还没发"                       │
        │   "多久能到"、"预计什么时候送达"、"还要几天"                     │
        │                                                                 │
        │ 场景 C · 异常物流反馈：                                        │
        │   "物流怎么不动了"、"快递卡住了"、"好几天没更新了"               │
        │   "包裹是不是丢了"、"物流异常"                                  │
        │                                                                 │
        │ 场景 D · 买家提供订单号并要求查询：                             │
        │   "ORD-123 查一下"、"订单123的状态"、"帮我查ORD-404"            │
        └─────────────────────────────────────────────────────────────────┘
        注意：若买家未提供订单号，大模型应先引导买家提供订单号后再调用本工具。

    Args:
        order_id (str):
            订单编号，格式为 "ORD-{数字}"（如 "ORD-123"），大小写严格敏感。
            大模型应从买家对话中提取订单号，提取规则：
            - 买家输入 "ORD-123"、"订单123"、"123号订单" → 均归一化为 "ORD-123"
            - 买家未提供订单号 → 不可调用本工具，应向买家询问订单号
            约束：必须是以 "ORD-" 开头、后接数字的字符串，否则返回默认处理中状态。

    Returns:
        dict —— 统一 JSON 响应体，大模型生成回复时按以下路径提取数据：
        ┌─────────────────────────────────────────────────────────────────┐
        │ 顶层键（大模型回复话术生成指引）                                 │
        │                                                                 │
        │ ret["status"] (str)                                            │
        │   "success" → 物流正常，告知买家当前派送状态与预计送达时间       │
        │   "warning" → 物流异常，共情买家焦虑情绪，建议联系物流客服       │
        │   "info"    → 订单处理中，安抚买家耐心等待，说明待揽收现状       │
        │                                                                 │
        │ ret["message"] (str)                                            │
        │   人类可读的状态摘要，可直接嵌入客服回复话术或作为话术草稿       │
        │                                                                 │
        │ ret["data"] (dict) —— 结构化详情，供大模型按需提取关键字段      │
        │   ├─ order_id:       str   订单编号（原样回传，用于核对）       │
        │   ├─ order_status:   str   订单状态枚举值：                     │
        │   │      shipped    → 已发货，物流正常运输中                    │
        │   │      stalled    → 异常停滞，包裹在某个节点滞留              │
        │   │      processing → 处理中，仓库尚未出库                      │
        │   └─ logistics_info: dict  物流详情子对象，键说明如下：         │
        │        ├─ carrier:              str  快递公司名称（如顺丰速运） │
        │        ├─ tracking_number:      str  快递单号（可用于外链查询） │
        │        ├─ current_status:       str  当前物流节点描述（派送中） │
        │        ├─ estimated_delivery:   str  预计送达日期（YYYY-MM-DD） │
        │        ├─ last_update:          str  最后物流更新时间           │
        │        ├─ stagnant_hours:       float (仅异常时) 已停滞小时数   │
        │        └─ last_scan_location:   str   (仅异常时) 最后扫描位置   │
        └─────────────────────────────────────────────────────────────────┘
    """
    # ---------------------------------------------------------
    # 剧本分支一：ORD-123 —— 已发货，顺丰速运正在派送
    # ---------------------------------------------------------
    if order_id == "ORD-123":
        return {
            "status": "success",
            "message": "订单已发货，顺丰速运正在派送中，预计今日送达。",
            "data": {
                "order_id": "ORD-123",
                "order_status": "shipped",  # 已发货
                "logistics_info": {
                    "carrier": "顺丰速运",          # 承运快递公司
                    "tracking_number": "SF1234567890",  # 快递单号
                    "current_status": "派送中",     # 当前物流节点状态
                    "estimated_delivery": "2026-06-15",  # 预计送达日期
                    "last_update": "2026-06-14 09:30:00",  # 最后更新时间
                },
            },
        }

    # ---------------------------------------------------------
    # 剧本分支二：ORD-404 —— 异常停滞，转运中心滞留超 72 小时
    # ---------------------------------------------------------
    if order_id == "ORD-404":
        return {
            "status": "warning",
            "message": "订单异常：包裹在转运中心停滞超过 72 小时，建议联系物流客服核查。",
            "data": {
                "order_id": "ORD-404",
                "order_status": "stalled",  # 异常停滞
                "logistics_info": {
                    "carrier": "中通快递",
                    "tracking_number": "ZTO9876543210",
                    "current_status": "转运中心滞留",  # 停滞节点
                    "stagnant_hours": 72.5,  # 已停滞时长（小时）
                    "last_scan_location": "华南转运中心（广州）",  # 最后扫描位置
                    "last_update": "2026-06-11 06:15:00",  # 最后更新时间（72 小时前）
                },
            },
        }

    # ---------------------------------------------------------
    # 默认分支：订单处理中，待揽收
    # 覆盖所有未预设剧本的 order_id（如 ORD-001、ORD-999 等）
    # ---------------------------------------------------------
    return {
        "status": "info",
        "message": f"订单 {order_id} 正在处理中，等待仓库揽收，请稍后关注物流更新。",
        "data": {
            "order_id": order_id,
            "order_status": "processing",  # 处理中
            "logistics_info": {
                "carrier": "待分配",         # 尚未分配快递公司
                "tracking_number": None,     # 暂无快递单号
                "current_status": "待揽收",  # 仓库尚未出库
                "estimated_delivery": "待定",
                "last_update": None,
            },
        },
    }


