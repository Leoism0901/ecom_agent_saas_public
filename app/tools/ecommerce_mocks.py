# 2. 退款 / 退货拦截
# ============================================================

def refund_item(order_id: str, reason: str = "") -> dict:
    """
    【退款 / 退货拦截】对指定订单发起退款或退货拦截操作，返回处理结果与买家指引。

    触发意图（大模型 Tool-Calling 核心决策依据 —— 必须严格遵守）：
        大模型在接收到买家消息时，若消息语义匹配以下任一场景，**必须**调用本工具：
        ┌─────────────────────────────────────────────────────────────────┐
        │ 场景 A · 买家主动要求退款 / 退货 / 仅退款：                     │
        │   "我要退款"、"申请退货"、"不想要了帮我退"                       │
        │   "怎么退款"、"退货退款"、"仅退款"、"取消订单"                   │
        │                                                                 │
        │ 场景 B · 买家表达不满并要求补偿：                               │
        │   "东西坏了我要退"、"质量太差退款吧"、"收到的和图片不一样"       │
        │   "我要投诉并退款"、"不满意想退货"、"退钱"                       │
        │                                                                 │
        │ 场景 C · 买家询问退款进度：                                     │
        │   "我的退款处理了吗"、"退款到哪一步了"、"钱什么时候退回来"       │
        │   "退货审核通过了吗"、"退款进度查一下"                           │
        │                                                                 │
        │ 场景 D · 买家提供订单号 + 退款诉求：                            │
        │   "ORD-999 申请退款"、"订单123不要了退一下"                     │
        └─────────────────────────────────────────────────────────────────┘
        注意：reason 参数为可选，大模型应从买家消息中提取退款原因填入，若未明确说明则传空字符串。

    Args:
        order_id (str):
            订单编号，格式为 "ORD-{数字}"（如 "ORD-123"），大小写严格敏感。
            大模型提取规则同 get_order_status —— 买家输入 "订单123" → 归一化为 "ORD-123"。
            约束：必须是以 "ORD-" 开头的字符串。
        reason (str, 可选):
            买家填写的退款原因，大模型从对话中提取后填入。常见取值：
            "不想要了" / "商品破损" / "发错货了" / "质量问题" / "与描述不符" / "迟迟未发货" / ""（未说明）。
            该参数当前不参与 Mock 分支路由逻辑（仅 order_id 决定返回结果），
            但保留此参数供后续真实退款系统接入时使用，届时 reason 将影响审核结果。

    Returns:
        dict —— 统一 JSON 响应体，大模型生成回复时按以下路径提取数据：
        ┌─────────────────────────────────────────────────────────────────┐
        │ 顶层键（大模型回复话术生成指引）                                 │
        │                                                                 │
        │ ret["status"] (str)                                             │
        │   "success" → 退款成功，告知买家金额、渠道与预计到账时间         │
        │   "failed"  → 退款拦截失败，清晰说明原因并给出替代操作路径       │
        │   "pending" → 审核中，告知买家预计处理时效，安抚等待情绪         │
        │                                                                 │
        │ ret["message"] (str)                                            │
        │   退款处理结果的人类可读摘要，可直接嵌入客服话术或转发买家       │
        │                                                                 │
        │ ret["data"] (dict) —— 结构化退款详情，供大模型按需提取          │
        │   ├─ order_id:                  str   订单编号（原样回传）       │
        │   ├─ refund_status:             str   退款状态枚举值：           │
        │   │      refunded        → 已退款，款项已提交支付渠道           │
        │   │      intercept_failed → 拦截失败（已发货订单无法召回）      │
        │   │      under_review     → 审核中，等待系统处理                │
        │   ├─ refund_amount:            float 退款金额（元）              │
        │   ├─ refund_channel:           str   退款渠道（如微信支付原返）  │
        │   ├─ reason_accepted:           bool 系统是否接受退款原因        │
        │   ├─ expected_processing_hours: int   (审核中时) 预计处理时长    │
        │   ├─ estimated_arrival:        str   (已退款时) 到账时效说明     │
        │   └─ next_step:                str   买家下一步操作指引          │
        └─────────────────────────────────────────────────────────────────┘
    """
    # ---------------------------------------------------------
    # 剧本分支一：ORD-123 —— 已发货，拦截失败，引导买家拒收
    # ---------------------------------------------------------
    if order_id == "ORD-123":
        return {
            "status": "failed",
            "message": "退款拦截失败：包裹已出库并在运输途中，无法中途召回。请引导买家在快递到达时拒收，拒收后系统将自动触发退款。",
            "data": {
                "order_id": "ORD-123",
                "refund_status": "intercept_failed",  # 拦截失败
                "refund_amount": 0.0,  # 尚未退款，金额暂为 0
                "refund_channel": None,
                "reason_accepted": True,  # 退款原因合理，但物流状态不允许直接退款
                "next_step": "引导买家拒收包裹，拒收后系统自动触发退款流程，款项 3-5 个工作日原路返回。",
            },
        }

    # ---------------------------------------------------------
    # 剧本分支二：ORD-999 —— 未发货，退款成功，原路返回
    # ---------------------------------------------------------
    if order_id == "ORD-999":
        return {
            "status": "success",
            "message": "退款成功：订单未发货，拦截已生效，款项将原路返回至买家支付账户。",
            "data": {
                "order_id": "ORD-999",
                "refund_status": "refunded",  # 已退款
                "refund_amount": 299.00,  # 模拟退款金额（元）
                "refund_channel": "微信支付原路返回",  # 原支付渠道退回
                "reason_accepted": True,
                "estimated_arrival": "预计 1-3 个工作日内到账",  # 到账时效说明
                "next_step": "退款已提交银行处理，请提醒买家留意支付账户到账通知。",
            },
        }

    # ---------------------------------------------------------
    # 默认分支：退款审核中，24 小时内出结果
    # 覆盖所有未预设剧本的 order_id
    # ---------------------------------------------------------
    return {
        "status": "pending",
        "message": f"订单 {order_id} 的退款申请已提交，审核中，预计 24 小时内完成处理。",
        "data": {
            "order_id": order_id,
            "refund_status": "under_review",  # 审核中
            "refund_amount": 0.0,  # 审核未完成，金额待定
            "refund_channel": None,
            "reason_accepted": True,
            "expected_processing_hours": 24,  # 预计处理时长（小时）
            "next_step": "等待系统审核，审核通过后自动发起退款。买家可在「我的订单」中查看进度。",
        },
    }


