# 3. 运费规则查询
# ============================================================

def check_shipping_rules(item_id: str, province: str) -> dict:
    """
    【运费与发货规则查询】根据商品 ID 与收货省份，查询是否包邮、运费金额及预计发货时效。

    触发意图（大模型 Tool-Calling 核心决策依据 —— 必须严格遵守）：
        大模型在接收到买家消息时，若消息语义匹配以下任一场景，**必须**调用本工具：
        ┌─────────────────────────────────────────────────────────────────┐
        │ 场景 A · 买家询问运费相关：                                    │
        │   "包邮吗"、"运费多少"、"要不要邮费"                             │
        │   "新疆包邮吗"、"内蒙古运费多少钱"、"西藏发货要加钱吗"          │
        │   "偏远地区包不包邮"、"这边要补运费吗"                          │
        │                                                                 │
        │ 场景 B · 买家询问发货时效：                                    │
        │   "多久能发货"、"什么时候发"、"几天能发"                         │
        │   "今天能发吗"、"付款后多久发货"、"发货时间"                     │
        │                                                                 │
        │ 场景 C · 买家提供省份地址 + 运费 / 时效疑问：                  │
        │   "我在广东，包邮吗"、"发到西藏要几天"                          │
        │   "收货地址是新疆乌鲁木齐，运费怎么算"                          │
        └─────────────────────────────────────────────────────────────────┘
        注意：大模型必须从买家消息中提取 province 参数（省份名），
        若买家仅提及城市（如"乌鲁木齐"），大模型应映射为对应省份（如"新疆"）后再调用。
        若买家未提及任何地址信息，大模型应先询问收货地区再调用本工具。

    Args:
        item_id (str):
            商品编号 / SKU 编码，字符串类型。当前版本所有商品共用同一套运费模板
            （即 item_id 不影响 Mock 返回结果），但保留此参数供后续按商品差异化定价扩展。
            大模型从对话上下文中提取商品 ID，若无法获取可传空字符串 "" 或 "ITEM-UNKNOWN"。
        province (str):
            收货省份名称，字符串类型。大模型从买家消息中提取，提取与归一化规则：
            - 买家说"新疆" / "新疆维吾尔自治区" / "新疆乌鲁木齐" → province="新疆"
            - 买家说"西藏" / "西藏自治区" / "拉萨" → province="西藏"
            - 买家说"内蒙古" / "内蒙古自治区" / "呼和浩特" → province="内蒙古"
            - 买家说"广东" / "广东省" / "广州" → province="广东省"
            - 核心原则：函数内部使用子串匹配判断偏远地区（含"新疆"/"西藏"/"内蒙古"即命中），
              因此传入 "新疆维吾尔自治区" 和 "新疆" 效果相同，但建议传入标准化省份简称以提高匹配效率。

    Returns:
        dict —— 统一 JSON 响应体，大模型生成回复时按以下路径提取数据：
        ┌─────────────────────────────────────────────────────────────────┐
        │ 顶层键（大模型回复话术生成指引）                                 │
        │                                                                 │
        │ ret["status"] (str)                                             │
        │   "free_shipping"        → 包邮，告知买家零运费与发货时效       │
        │   "shipping_fee_required"→ 不包邮，清晰说明需补运费金额         │
        │                                                                 │
        │ ret["message"] (str)                                            │
        │   运费规则的人类可读摘要，可直接用于客服回复                     │
        │                                                                 │
        │ ret["data"] (dict) —— 结构化运费详情，供大模型按需提取          │
        │   ├─ item_id:         str   商品编号（原样回传）                 │
        │   ├─ province:        str   收货省份（原样回传）                 │
        │   ├─ is_remote:       bool  是否为偏远地区                      │
        │   │      True  → 新疆 / 西藏 / 内蒙古                           │
        │   │      False → 其他省份                                       │
        │   ├─ shipping_fee:    float 运费金额（元），包邮时为 0.00        │
        │   ├─ free_shipping:   bool  是否包邮                            │
        │   ├─ estimated_ship:  str   预计发货时效描述（如"48小时内"）     │
        │   └─ note:            str   补充说明（大促时效顺延 / 偏远延迟） │
        └─────────────────────────────────────────────────────────────────┘
    """
    # ---------------------------------------------------------
    # 偏远地区关键词列表 —— 匹配"新疆""西藏""内蒙古"三个省份
    # 使用子串匹配，兼容用户传入 "新疆维吾尔自治区" 等完整行政区划名
    # ---------------------------------------------------------
    REMOTE_PROVINCES = ("新疆", "西藏", "内蒙古")

    # 判断当前省份是否命中偏远地区关键词
    is_remote = any(keyword in province for keyword in REMOTE_PROVINCES)

    # ---------------------------------------------------------
    # 剧本分支一：偏远地区 —— 不包邮，需补 15 元运费
    # ---------------------------------------------------------
    if is_remote:
        return {
            "status": "shipping_fee_required",
            "message": f"收货地址位于「{province}」，属于偏远地区，不包邮，需补运费 15 元。",
            "data": {
                "item_id": item_id,
                "province": province,
                "is_remote": True,
                "shipping_fee": 15.00,     # 偏远地区补邮费（元）
                "free_shipping": False,     # 不包邮
                "estimated_ship": "付款后 72 小时内发货",  # 偏远地区发货时效略长
                "note": "偏远地区运输时效可能延长 1-3 天，敬请谅解。",
            },
        }

    # ---------------------------------------------------------
    # 默认分支：非偏远地区 —— 包邮，48 小时内发货
    # ---------------------------------------------------------
    return {
        "status": "free_shipping",
        "message": f"收货地址位于「{province}」，享受包邮服务，预计 48 小时内发货。",
        "data": {
            "item_id": item_id,
            "province": province,
            "is_remote": False,
            "shipping_fee": 0.00,          # 包邮，运费为零
            "free_shipping": True,          # 包邮
            "estimated_ship": "付款后 48 小时内发货",
            "note": "如遇大促活动，发货时效可能顺延，请以实际物流信息为准。",
        },
    }


